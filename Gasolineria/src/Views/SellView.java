    private void TipoComActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TipoComActionPerformed
        int i=TipoCom.getSelectedIndex();
        PrecioG.setText(String.valueOf(controlador.getCombustible(i).getPrecioG()));
        float temp=Float.parseFloat(Galones.getText());
        PrecioV.setText(String.valueOf((controlador.getCombustible(i).getPrecioG())*temp));
    }//GEN-LAST:event_TipoComActionPerformed

    private void RegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistroActionPerformed
        String nombre=controlador.getCombustible(TipoCom.getSelectedIndex()).getNombre();
        float cantidad=Float.parseFloat(Galones.getText());
        float total=(controlador.getCombustible(TipoCom.getSelectedIndex()).getPrecioG()*cantidad);
        if(cantidad>0.1){
        controlador.Insertar(nombre,cantidad,total);
        System.out.println("Insertado con exito");
        }else{
        JOptionPane.showMessageDialog(null, "La cantidad de galones debe ser mayor a 0.1 ");
        }
    }//GEN-LAST:event_RegistroActionPerformed

